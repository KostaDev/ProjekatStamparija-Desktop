    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            String dat = txtDatumNarucivanja.getText();
            
            boolean valid = true;
            
            Date datum = null;
            try {
                datum = sdf.parse(dat);
                errDatum.setVisible(false);
            } catch (ParseException ex) {
                valid = false;
                errDatum.setVisible(true);                
            }
            Klijent k = (Klijent) cmbKlijent.getSelectedItem();
            Stampanje s = (Stampanje) cmbStampanje.getSelectedItem();
            
            if (!valid) {                
                return;
            }
            
            Narucivanje nar = new Narucivanje(1, datum, s, k);
            
            ServerskiOdgovor so = Kontroler.getInstance().sacuvajNarucivanje(nar);
            JOptionPane.showMessageDialog(this, so.getPoruka());
            zatvoriFormu();
        } catch (Exception ex) {
            Logger.getLogger(FormaUnosNarucivanja.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

