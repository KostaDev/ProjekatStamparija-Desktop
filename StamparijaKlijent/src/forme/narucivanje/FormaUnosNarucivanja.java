    private void popuniKomboKlijenata() {
        List<IOpstiDomenskiObjekat> lista = Kontroler.getInstance().dajListuKlijenata();
        cmbKlijent.removeAllItems();

        for (IOpstiDomenskiObjekat odo : lista) {
            cmbKlijent.addItem(odo);
        }
    }

    private void popuniKomboStampanja() {
        List<IOpstiDomenskiObjekat> lista = Kontroler.getInstance().dajListuStampanja();
        cmbStampanje.removeAllItems();

        for (IOpstiDomenskiObjekat odo : lista) {
            cmbStampanje.addItem(odo);
        }
    }

    void setFormuZaBrisanje(Narucivanje naru) {
        this.n = naru;
        btnObrisi.setVisible(true);
        btnSacuvaj.setVisible(false);
        txtDatumNarucivanja.setEditable(false);
        txtDatumNarucivanja.setText(new SimpleDateFormat("dd.MM.yyyy").format(naru.getDatumNarucivanja()));
        cmbStampanje.setSelectedItem(naru.getStampanje());
        cmbStampanje.setEnabled(false);
        cmbKlijent.setSelectedItem(naru.getKlijent());
        cmbKlijent.setEnabled(false);
    }

    private void zatvoriFormu() {
        this.setVisible(false);
        this.dispose();
    }

    private void sakrijErrLabele() {
        errDatum.setVisible(false);
    }
